package fypms;

/**
 * Enum class for Student's Status
 * @version 1.0
 */
public enum StudentStatus {

    /**
     * New
     */
    NEW,

    /**
     * Pending
     */
    PENDING,

    /**
     * Pending for title change
     */
    PENDINGTITLE,

    /**
     * Pending for deregistration
     */
    PENDINGDEALLOCATION,
    
    /**
     * Registered
     */
    REGISTERED,

    /**
     * De-registered
     */
    DEREGISTERED
}


